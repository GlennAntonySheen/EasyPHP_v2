Welcome to the help document for the Smart Counties R Us application prototype. This document will provide guidance on how to use the application and its features.

</br><h3>SMEs and Products</h3>
The Smart Counties R Us application allows small to medium enterprises (SMEs) to register details of their smart products. To register a product, SMEs must provide the following details:

Company name
Contact phone number
Product name
Short description of the product's purpose
Size and material of the product (if applicable)
Type/environment of the product (such as exterior fixture, household appliance, etc.)
Price band: low-range, mid-range, or high-range.
Please note that each product can only be registered once by one SME.

</br><h3>Residents</h3>
The application is designed for residents to browse smart products and register their interest in those they feel would be beneficial to their local area. To vote on a product, residents must register on the application and provide the following details:

Area of residence (from a dropdown menu provided by the local council)
Gender
Age bracket.
Residents can vote on as many products as they are interested in, but they must indicate whether the product's price band is something they would be prepared to pay for (a "Yes/No" field).

</br><h3>Votes</h3>
The application records residents' votes to establish potential demand, supply, and marketing for smart products. SMEs can monitor residents' votes to determine which products are of interest to potential customers.

</br><h3>Residing Areas</h3>

The local council is responsible for signing up (adding) their residing area on the application so that their residents can utilize the application's facilities. The council will also be able to use the platform to monitor which smart products residents in their area are interested in, and they may also use this information to work with SMEs in promoting particular 'smart' initiatives to local residents.

Data Model

The application's data model includes several entities, including:

SMEs: register their company and product details
Products: details of each smart product registered by an SME
Residents: register their interest in smart products and provide details of their area of residence, gender, and age bracket
Areas: a dropdown menu of areas where residents can indicate their area of residence.
Other entities may be included in the data model to improve the application's usability and minimize user input errors.

</br><h3>Conclusion</h3>

The Smart Counties R Us application is designed to promote the 'smart city' agenda in suburban and rural areas by combining a facility for local councils to track the possible utilization of 'smart technology' with a platform where small to medium enterprises (SMEs) can promote relevant 'smart' products in a local area. By following the guidance provided in this help document, you can make the most of the application's features and contribute to promoting the use of smart technology in your local community.

